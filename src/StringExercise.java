public class StringExercise {
    public static void main(String[] args) {
        String twoLines = "We don't need no education \n" +
                "We don't need no thought control";
        System.out.println(twoLines);

        String backSlash = "In windows, the main drive is usually C:\\";
        System.out.println(backSlash);

        String doubleQuotes = "Check \"this\" out! \"s inside of \"s!";
        System.out.println(doubleQuotes);

        String backSlashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backSlashes);
    }
}
